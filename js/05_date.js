// Date Object
var d = new Date();
console.log(d)

// Set Methods
d.setFullYear(1996)
d.setMonth(10)
d.setDate(25)

// Get Methods
console.log(d.getFullYear())
console.log(d.getMonth())
console.log(d.getDate())
console.log(d.getHours())
console.log(d.getMinutes())
console.log(d.getSeconds())
console.log(d.getMilliseconds())
console.log(d.getDay())